//Arithmetic operator:
// + - * / %  ++ --


var a = 11
var b = 3

console.log(a/b)
console.log(a % b)

//Incremental
//pre-increment/ post-increment

console.log(a++)
console.log(++a)

console.log(--b)
console.log(b--)


//Assignment operator:

var a = 10
var b = 22

a +=b
console.log(a)

a -=b
console.log(a)

a *=b
console.log(a)

a /=b
console.log(a)

a %=b
console.log(a)


//Comparison operator:

var a = 10
var b = 20

console.log(a == b) //false
console.log(a != b) //true

console.log(a > b) //false
console.log(a < b) //true

console.log(a >= b) //false
console.log(a <= b) //true

var c = '50'
var d = 50

console.log(c == d)
console.log(c === d)
console.log(c !== d)

